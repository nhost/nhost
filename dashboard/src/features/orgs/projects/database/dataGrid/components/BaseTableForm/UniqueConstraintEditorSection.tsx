import { ArrowDown, ArrowUp, Plus, X } from 'lucide-react';
import { useFieldArray, useFormContext, useFormState, useWatch } from 'react-hook-form';
import { useDialog } from '@/components/common/DialogProvider';
import { FormInput } from '@/components/form/FormInput';
import { Button } from '@/components/ui/v3/button';
import { Label } from '@/components/ui/v3/label';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/v3/select';
import { createConstraintFormId } from '@/features/orgs/projects/database/dataGrid/components/BaseTableForm/formReferences';
import type {
  DatabaseColumn,
  FormUniqueConstraint,
} from '@/features/orgs/projects/database/dataGrid/types/dataBrowser';

function ConstraintErrorMessage() {
  const { errors } = useFormState({ name: 'uniqueConstraints' });
  const error = errors.uniqueConstraints;
  const message =
    typeof error?.message === 'string'
      ? error.message
      : typeof error?.root?.message === 'string'
        ? error.root.message
        : undefined;

  return message ? (
    <p className="font-medium text-destructive text-sm">{message}</p>
  ) : null;
}

export default function UniqueConstraintEditorSection() {
  const { openAlertDialog } = useDialog();
  const { control, getValues, setValue } = useFormContext();
  const { fields, append, remove } = useFieldArray({
    control,
    name: 'uniqueConstraints',
    keyName: 'fieldId',
  });
  const columns = (useWatch({ name: 'columns' }) ?? []) as DatabaseColumn[];
  const constraints = (useWatch({ name: 'uniqueConstraints' }) ??
    []) as FormUniqueConstraint[];
  const availableColumns = columns.filter(({ formReference }) => formReference);

  const updateReferences = (index: number, columnReferences: string[]) => {
    setValue(`uniqueConstraints.${index}.columnReferences`, columnReferences, {
      shouldDirty: true,
      shouldValidate: true,
    });
  };

  return (
    <div className="space-y-4 px-6">
      <p className="text-muted-foreground text-sm">
        UNIQUE constraints may contain one or more columns. Column order is
        preserved.
      </p>

      {fields.map((field, constraintIndex) => {
        const constraint = constraints[constraintIndex];
        const references = constraint?.columnReferences ?? [];

        return (
          <div
            key={field.fieldId}
            className="space-y-3 rounded-md border border-border p-4"
          >
            <div className="flex items-start gap-2">
              <div className="flex-1">
                <FormInput
                  control={control}
                  name={`uniqueConstraints.${constraintIndex}.name`}
                  label={constraint?.originalName ? 'Name' : 'Name (optional)'}
                  placeholder="Generated by PostgreSQL when empty"
                  autoComplete="off"
                  className="border-border"
                />
              </div>
              <Button
                type="button"
                variant="outline"
                size="icon"
                aria-label="Remove unique constraint"
                onClick={() =>
                  openAlertDialog({
                    title: 'Remove unique constraint?',
                    payload:
                      'This UNIQUE constraint will be removed when you save the table.',
                    props: {
                      primaryButtonText: 'Remove',
                      primaryButtonColor: 'error',
                      onPrimaryAction: () => remove(constraintIndex),
                    },
                  })
                }
              >
                <X className="h-4 w-4" />
              </Button>
            </div>

            <div className="space-y-2">
              <Label>Columns</Label>
              {references.map((reference, columnIndex) => {
                const selectedColumn = columns.find(
                  ({ formReference }) => formReference === reference,
                );

                return (
                  <div
                    className="flex items-center gap-2"
                    key={`${field.fieldId}-${columnIndex}`}
                  >
                    <Select
                      value={reference}
                      onValueChange={(value) => {
                        const next = [...references];
                        next[columnIndex] = value;
                        updateReferences(constraintIndex, next);
                      }}
                    >
                      <SelectTrigger
                        className="h-10 flex-1"
                        aria-label={`Unique constraint column ${columnIndex + 1}`}
                      >
                        <SelectValue />
                      </SelectTrigger>
                      <SelectContent>
                        {!selectedColumn && (
                          <SelectItem value={reference} disabled>
                            Missing column ({reference})
                          </SelectItem>
                        )}
                        {availableColumns.map((column) => (
                          <SelectItem
                            key={column.formReference}
                            value={column.formReference!}
                            disabled={
                              references.includes(column.formReference!) &&
                              column.formReference !== reference
                            }
                          >
                            {column.name || 'Unnamed column'}
                          </SelectItem>
                        ))}
                      </SelectContent>
                    </Select>
                    <Button
                      type="button"
                      variant="ghost"
                      size="icon"
                      aria-label="Move unique column up"
                      disabled={columnIndex === 0}
                      onClick={() => {
                        const next = [...references];
                        [next[columnIndex - 1], next[columnIndex]] = [
                          next[columnIndex],
                          next[columnIndex - 1],
                        ];
                        updateReferences(constraintIndex, next);
                      }}
                    >
                      <ArrowUp className="h-4 w-4" />
                    </Button>
                    <Button
                      type="button"
                      variant="ghost"
                      size="icon"
                      aria-label="Move unique column down"
                      disabled={columnIndex === references.length - 1}
                      onClick={() => {
                        const next = [...references];
                        [next[columnIndex], next[columnIndex + 1]] = [
                          next[columnIndex + 1],
                          next[columnIndex],
                        ];
                        updateReferences(constraintIndex, next);
                      }}
                    >
                      <ArrowDown className="h-4 w-4" />
                    </Button>
                    <Button
                      type="button"
                      variant="ghost"
                      size="icon"
                      aria-label="Remove unique column"
                      onClick={() =>
                        updateReferences(
                          constraintIndex,
                          references.filter((_, index) => index !== columnIndex),
                        )
                      }
                    >
                      <X className="h-4 w-4" />
                    </Button>
                  </div>
                );
              })}
              <Button
                type="button"
                variant="ghost"
                size="sm"
                className="gap-2 text-primary"
                disabled={references.length >= availableColumns.length}
                onClick={() => {
                  const nextColumn = availableColumns.find(
                    ({ formReference }) =>
                      !references.includes(formReference!),
                  );
                  if (nextColumn?.formReference) {
                    updateReferences(constraintIndex, [
                      ...references,
                      nextColumn.formReference,
                    ]);
                  }
                }}
              >
                <Plus className="h-4 w-4" />
                Add Column
              </Button>
            </div>
          </div>
        );
      })}

      <ConstraintErrorMessage />

      <Button
        type="button"
        variant="ghost"
        size="sm"
        className="gap-2 text-primary"
        disabled={availableColumns.length === 0}
        onClick={() => {
          const firstColumnReference = getValues('columns')?.find(
            ({ formReference }: DatabaseColumn) => formReference,
          )?.formReference;
          append({
            id: createConstraintFormId(),
            name: '',
            columnReferences: firstColumnReference
              ? [firstColumnReference]
              : [],
          });
        }}
      >
        <Plus className="h-4 w-4" />
        Add Unique Constraint
      </Button>
    </div>
  );
}
